const LAYOUT_ROW_HERO = 'hero';
const LAYOUT_ROW_TEXT_AND_IMAGE = 'text_and_image';
const LAYOUT_ROW_HALF_TEXT_HALF_IMAGE = 'half_text_half_image';
const LAYOUT_ROW_MONTHS = 'months';

function primeiv_get_page_builder_section_class( $row_layout ){
    $full_width_layouts = [LAYOUT_ROW_HERO, LAYOUT_ROW_TEXT_AND_IMAGE, LAYOUT_ROW_HALF_TEXT_HALF_IMAGE, LAYOUT_ROW_MONTHS];

    $class = 'primeiv-section';

    if( ! in_array( $row_layout, $full_width_layouts ) ) {
        $class .= ' container';
    }

    return $class;
}

function primeiv_print_button( $title, $url, $in_wrapper = true ){
    $html = '';
    
    if( $in_wrapper ) {
        $html .= '<div class="primeiv-button-wrapper">';
    }

    $html .= sprintf('<a href="%s" class="fusion-bar-highlight primeiv-button"><span class="menu-text fusion-button button-default button-large">%s</span></a>', esc_url( $url ), esc_html($title));

    if( $in_wrapper ) {
        $html .= '</div>';
    }
    echo $html;
}

function primeiv_get_section_root_class($template_slug) {
    return sprintf( 'primeiv-%s', esc_attr( $template_slug ) );
}

add_shortcode('primeiv-tooltip', function ( $atts, $content ){
    $a = shortcode_atts( array(
        'number' => 1,
        'url' => '',
    ), $atts );
    if( $a['url'] !== '') {
        $content .= '<a href="' .  esc_url( $a['url'] ) . '" target="_blank" class="tooltip-anchor"></a>';
    }
    ob_start();
    ?><span class="primeiv-tooltip" title="<?php echo htmlspecialchars($content) ?>"><sup>[<?php echo $a['number']; ?>]</sup></span>
    <?php
    return ob_get_clean();
});

